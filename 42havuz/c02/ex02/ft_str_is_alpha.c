int	ft_str_is_alpha(char *str)

{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((65 > str[i] || str[i] > 90) && (122 < str[i] || str[i] < 97))
		{
			return (0);
		}
		else
		{
			i++;
		}
	}
	return (1);
}
