int	ft_str_is_numeric(char *str)
{
	int	n;

	n = 0;
	while (str[n] != '\0')
	{
		if ((str[n] < '0') || ('9' < str[n]))
		{
			return (0);
		}
		else
		{
			n++;
		}
	}
	return (1);
}
